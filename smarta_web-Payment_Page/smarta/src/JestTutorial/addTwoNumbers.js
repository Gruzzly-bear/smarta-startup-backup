function addTwoNumbers(first, second) {
    /*
    Method: 
      Adds two numbers together
    Inputs: 
      first = description: an integer to be added, type: int
      second = description: an integer to be added, type: int
    Output: 
      an integer which is the summation of the inputs
    */
    return first + second;
  }

  module.exports = addTwoNumbers;